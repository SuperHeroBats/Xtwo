package com.xtwo.android;

public class Memory {
    private String name;
    private int imgageId;

    public Memory(String name, int imgageId) {
        this.name = name;
        this.imgageId = imgageId;
    }

    public String getName() {
        return name;
    }

    public int getImgageId() {
        return imgageId;
    }
}
